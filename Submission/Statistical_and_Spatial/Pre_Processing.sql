--Create a table with distinct Nairobi UID's to filter Kenya file
CREATE TABLE PUBLIC.nairobidistinctuid AS
SELECT DISTINCT osm_uid
FROM (
	SELECT osm_uid
	FROM PUBLIC.nairobihot_line
	
	UNION
	
	SELECT osm_uid
	FROM PUBLIC.nairobihot_point
	
	UNION
	
	SELECT osm_uid
	FROM PUBLIC.nairobihot_polygon
	) AS userid;

--Identify bulk imports
CREATE TABLE n_bulkcheck_polygon AS (
	SELECT osm_timestamp::DATE,
	osm_uid,
	count(*) FROM PUBLIC.nairobihot_polygon GROUP BY osm_timestamp::DATE,
	osm_uid ORDER BY count(*) DESC
	);

CREATE TABLE n_bulkcheck_point AS (
	SELECT osm_timestamp::DATE,
	osm_uid,
	count(*) FROM PUBLIC.nairobihot_point GROUP BY osm_timestamp::DATE,
	osm_uid ORDER BY count(*) DESC
	);

CREATE TABLE n_bulkcheck_line AS (
	SELECT osm_timestamp::DATE,
	osm_uid,
	count(*) FROM PUBLIC.nairobihot_line GROUP BY osm_timestamp::DATE,
	osm_uid ORDER BY count(*) DESC
	);

